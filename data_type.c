#include <stdio.h>

int main() {
    
    int a = 10;
    float b = 10.5;
    char c = 'a';
    double d = 10.5;
    long long int e = 1000000000000000000;
    
    printf("The value of a is %d\n", a);
    printf("The value of b is %f\n", b);
    printf("The value of c is %c\n", c);
    printf("The value of d is %lf\n", d);
    printf("The value of e is %lld\n", e);
    return 0;
}